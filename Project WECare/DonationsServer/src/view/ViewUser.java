    public ViewUser(String nic) {
        initComponents();
        setLocationRelativeTo(null);
        try {
            UserModel user = userControllerImpl.getUser(nic);
            profPicLb5.setIcon(ImageHandler.scaleToSpace(user.getProfpic(), 168, 156));
            fNameTx.setText(user.getfName());
            lNameTx.setText(user.getlName());
            nicTx.setText(user.getNic());
            tpTx.setText(Integer.toString(user.getTp()));
            mailTx.setText(user.getMail());
            addressTx.setText(user.getAddress());
            
            ArrayList<DonModel> donations = donControllerImpl.getDonation(nic);
            if (donations.size() > 0) {
                ((GridLayout)donPan.getLayout()).setRows(donations.size());
                donPan.setSize(350, 100*donations.size());
                for (DonModel dModel : donations) {
                    donPan.add(new DonPanel(dModel));
                }
                donPan.repaint();
                donPan.validate();
                noDonLb.setVisible(false);
            }else{
                noDonLb.setVisible(true);
            }
            
            ArrayList<DonModel> reqDoantions = donControllerImpl.getReqDoantions(nic);
            if (reqDoantions.size() > 0) {
                ((GridLayout)reqPan.getLayout()).setRows(reqDoantions.size());
                reqPan.setSize(350, 100*reqDoantions.size());
                for (DonModel dModel : reqDoantions) {
                    reqPan.add(new DonPanel(dModel));
                }
                reqPan.repaint();
                reqPan.validate();
                noReqLb.setVisible(false);
                
            }else{
                noReqLb.setVisible(true);
            }
            
        } catch (RemoteException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ViewUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

