    private void profPicLbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_profPicLbMouseClicked
        try {
            profPic = ImageHandler.importImage();
            if (profPic != null) {
                ImageIcon scaleImage = ImageHandler.scaleToSpace(profPic, 250, 250);
                profPicLb.setIcon(scaleImage);
            }
            enableCreateUpdateBtns();
        } catch (IOException ex) {
            Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Please insert an image", "Error",  JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_profPicLbMouseClicked

    private void createBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtActionPerformed
        if(nicTx.getText().length() < 10){
            JOptionPane.showMessageDialog(this, "There should be 9 numbers and one character in NIC number...", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(tpTx.getText().length() < 10){
            JOptionPane.showMessageDialog(this, "There should be 10 numbers in TP number...", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!Validation.isMailValid(mailTx.getText())){
            JOptionPane.showMessageDialog(this, "Please Enter a valid Email...", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!passwordMatches()){
            JOptionPane.showMessageDialog(this, "Passwor and its confirmation is not matched...", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                UserController userController = ServerConnector.getServerConnector().getUserController();
                boolean addNewUser = userController.addNewUser(new UserModel(fNameTx.getText(), lNameTx.getText(), nicTx.getText(), Integer.parseInt(tpTx.getText()), mailTx.getText(), addressTx.getText(), new String(pwTx.getPassword()), profPic));
                if(addNewUser){
                    JOptionPane.showMessageDialog(this, "Account Created Successfully...", "", JOptionPane.INFORMATION_MESSAGE);
                    new Thread(){

                        @Override
                        public void run() {
                            try {
                                EMAIL.send(mailTx.getText(), "Welcome to WECare", "Congratulations! You've registered an account successfully.Thank you for joining us!\n" +
"Now you can Donate, View other donates and request fro them through WECare.");
                            } catch (MessagingException ex) {
                                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                        
                    }.start();
                }else{
                    JOptionPane.showMessageDialog(this, "Couldn't Create the Account...", "", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NotBoundException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (MalformedURLException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (RemoteException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            clearAll();
            dispose();
        }
    }//GEN-LAST:event_createBtActionPerformed

