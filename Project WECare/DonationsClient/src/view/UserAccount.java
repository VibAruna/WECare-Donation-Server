    private boolean passwordMatches() {
        char[] pw = pwTx.getPassword();
        char[] conPw = conPwTx.getPassword();
        if(pw.length != conPw.length){
            return false;
        }
        
        for(int i = 0; i < pw.length; i++){
            if (pw[i] != conPw[i]) {
                return false;
            }
        }
        
        return true;
    }
    
