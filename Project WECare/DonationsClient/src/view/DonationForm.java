    private void doaneBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doaneBtActionPerformed
        DonModel donModel = new DonModel(donId, donTx.getText(), catCombo.getSelectedItem().toString(), descTx.getText(), donNic, " ", image);
        try {
            boolean donate = ServerConnector.getServerConnector().getDonController().donate(donModel);
            if (donate) {
                JOptionPane.showMessageDialog(this, "Your donation is uploaded to the server...", "Done", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Uploading Failed", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DonationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_doaneBtActionPerformed

