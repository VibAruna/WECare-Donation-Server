    private void imgBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imgBtnMouseClicked
        try {
            image = ImageHandler.importImage();
            if (image != null) {
                ImageIcon scaleImage = ImageHandler.scaleToSpace(image, 250, 250);
                imgBtn.setIcon(scaleImage);
            }
            enabledonateeBtn();
        } catch (IOException ex) {
            Logger.getLogger(UserAccount.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Please insert an image", "Error",  JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_imgBtnMouseClicked

